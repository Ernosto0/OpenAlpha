class ReportWriterAgent(BaseAgent[FinalReport]):
    name = "report_writer_agent"
    output_schema = FinalReport

    def __init__(
        self,
        *,
        llm_provider: BaseLLMProvider | None = None,
        temperature: float = 0.2,
        max_output_tokens: int = 5000,
        **kwargs: Any,
    ) -> None:
        super().__init__(**kwargs)
        self.llm_provider = llm_provider
        self.temperature = temperature
        self.max_output_tokens = max_output_tokens

    async def execute(self, context: AnalysisContext) -> AgentExecutionPayload:
        if context.thesis_output is None:
            output = self._fallback_report(context)
            context.final_report = output
            return AgentExecutionPayload(
                status="partial",
                provider="deterministic",
                model="deterministic",
                cost_type="deterministic",
                output=output,
                data_used=self._data_used(context),
                warnings=self._report_warnings(context),
            )

        provider = self.llm_provider or self._create_llm_provider(
            context.request.llm_provider
        )
        try:
            result = await provider.generate_json(
                messages=[
                    {"role": "system", "content": REPORT_WRITER_AGENT_SYSTEM_PROMPT},
                    {"role": "user", "content": self.build_user_prompt(context)},
                ],
                output_schema=FinalReport,
                model=context.request.llm_model,
                agent_name=self.name,
                temperature=self.temperature,
                max_output_tokens=self.max_output_tokens,
            )
        except LLMProviderError as exc:
            if self._should_stop_on_llm_error(exc):
                raise
            output = self._fallback_report(
                context,
                reason=(
                    "The Report Writer Agent used a deterministic fallback because "
                    f"the LLM request failed: {exc}"
                ),
            )
            context.final_report = output
            return AgentExecutionPayload(
                status="partial",
                provider="deterministic",
                model="deterministic",
                cost_type="deterministic",
                output=output,
                data_used=self._data_used(context),
                warnings=output.warnings,
            )

        output = self.validate_output(result.content)
        if not isinstance(output, FinalReport):
            raise TypeError("report writer agent returned an unexpected output type")

        enriched_output = output.model_copy(
            update={
                "symbol": context.request.symbol,
                "market": context.request.market,
                "horizon": context.request.horizon,
                "company_name": self._company_name(context) or output.company_name,
                "created_at": context.updated_at,
                "latest_close": self._latest_close_value(context),
                "disclaimer": DISCLAIMER,
                "cost_breakdown": self._cost_breakdown(
                    context,
                    extra_trace=CostTrace(
                        provider=result.provider,
                        model=result.model,
                        agent_name=self.name,
                        input_tokens=result.input_tokens,
                        output_tokens=result.output_tokens,
                        estimated_cost_usd=result.estimated_cost_usd,
                        cost_type=result.cost_type,
                        duration_ms=result.duration_ms,
                        warnings=result.warnings,
                        parsing_errors=result.parsing_errors,
                    ),
                ),
                "warnings": self._dedupe(
                    [
                        *output.warnings,
                        *self._report_warnings(context),
                    ]
                ),
            }
        )

        context.final_report = enriched_output

        return AgentExecutionPayload(
            status="completed",
            provider=result.provider,
            model=result.model,
            input_tokens=result.input_tokens,
            output_tokens=result.output_tokens,
            estimated_cost_usd=result.estimated_cost_usd,
            cost_type=result.cost_type,
            duration_ms=result.duration_ms,
            output=enriched_output,
            data_used=self._data_used(context),
            warnings=enriched_output.warnings,
            parsing_errors=result.parsing_errors,
        )

    def build_user_prompt(self, context: AnalysisContext) -> str:
        prompt = REPORT_WRITER_AGENT_USER_PROMPT.format(
            symbol=context.request.symbol,
            company_name=self._company_name(context) or "Unknown",
            market=context.request.market,
            created_at=context.updated_at.isoformat(),
            horizon=context.request.horizon,
            analysis_depth=context.request.depth,
            latest_close=self._latest_close(context),
            latest_price_timestamp=self._latest_price_timestamp(context),
            data_collector_output=self._data_collector_output_text(context),
            technical_agent_output=self._technical_output_text(context.technical_output),
            news_sentiment_agent_output=self._news_output_text(
                context.news_sentiment_output
            ),
            bull_case_agent_output=self._bull_case_output_text(context.bull_case_output),
            bear_case_agent_output=self._bear_case_output_text(context.bear_case_output),
            risk_review_agent_output=self._risk_review_output_text(
                context.risk_review_output
            ),
            thesis_agent_output=self._thesis_output_text(context.thesis_output),
            data_quality_score=self._data_quality_score(context),
            price_data_status=self._price_data_status(context),
            news_data_status=self._news_data_status(context),
            company_profile_status=self._company_profile_status(context),
            missing_data=self._join_or_none(self._missing_data(context)),
            provider_names=self._join_or_none(self._provider_names(context)),
            data_quality_warnings=self._join_or_none(self._data_quality_warnings(context)),
            source_metadata=json.dumps(
                [source.model_dump(mode="json") for source in self._source_items(context)],
                indent=2,
                sort_keys=True,
            ),
            cost_tracking_metadata=json.dumps(
                self._cost_breakdown(context).model_dump(mode="json"),
                indent=2,
                sort_keys=True,
            ),
            failed_agent_outputs=self._join_or_none(self._failed_or_missing_outputs(context)),
        )

        structured_payload = {
            "data_collector_output": self._data_collector_output_payload(context),
            "technical_output": self._model_dump_or_none(context.technical_output),
            "news_sentiment_output": self._model_dump_or_none(
                context.news_sentiment_output
            ),
            "bull_case_output": self._model_dump_or_none(context.bull_case_output),
            "bear_case_output": self._model_dump_or_none(context.bear_case_output),
            "risk_review_output": self._model_dump_or_none(context.risk_review_output),
            "thesis_output": self._model_dump_or_none(context.thesis_output),
            "data_quality_section": self._data_quality_section(context).model_dump(
                mode="json"
            ),
            "source_section": [
                item.model_dump(mode="json") for item in self._source_items(context)
            ],
            "cost_breakdown": self._cost_breakdown(context).model_dump(mode="json"),
            "warnings": self._report_warnings(context),
        }
        prompt += (
            "\n\nStructured report payload:\n"
            f"{json.dumps(structured_payload, indent=2, sort_keys=True)}"
        )
        if context.request.custom_question:
            prompt += (
                "\n\nUser research focus: "
                f"{context.request.custom_question.strip()}"
            )
        return prompt

    def _fallback_report(
        self,
        context: AnalysisContext,
        *,
        reason: str | None = None,
    ) -> FinalReport:
        company_name = self._company_name(context) or "Unknown"
        warnings = self._report_warnings(context)
        if reason:
            warnings = self._dedupe([reason, *warnings])
        thesis_output = context.thesis_output
        overall_view = (
            thesis_output.overall_view if thesis_output is not None else "insufficient_data"
        )
        confidence = thesis_output.confidence if thesis_output is not None else 0.1
        investment_thesis = (
            thesis_output.thesis
            if thesis_output is not None
            else (
                "A reliable final investment thesis is unavailable because the "
                "Thesis Agent output is missing from the analysis context."
            )
        )
        base_case = (
            thesis_output.base_case
            if thesis_output is not None
            else "No reliable base case can be formed without a completed thesis."
        )
        bull_case_summary = (
            thesis_output.bull_case_summary
            if thesis_output is not None
            else (
                context.bull_case_output.bull_case
                if context.bull_case_output is not None
                else "Bull case output is unavailable."
            )
        )
        bear_case_summary = (
            thesis_output.bear_case_summary
            if thesis_output is not None
            else (
                context.bear_case_output.bear_case
                if context.bear_case_output is not None
                else "Bear case output is unavailable."
            )
        )
        what_to_watch = (
            list(thesis_output.what_to_watch)
            if thesis_output is not None
            else [
                "Whether the missing thesis output can be regenerated.",
                "Whether missing upstream agents can be rerun successfully.",
                "Whether data quality improves enough for a complete report.",
            ]
        )
        executive_summary = (
            "The report was assembled from deterministic fallbacks because the final "
            "LLM-based report writing step could not be completed."
            if reason
            else (
                "The report could not be completed with a reliable final AI view "
                "because the thesis output is missing. Available upstream research "
                "is preserved below, but the result should be treated as incomplete."
            )
        )
        report = FinalReport(
            title=f"Equity Research Report: {context.request.symbol}",
            symbol=context.request.symbol,
            company_name=company_name,
            market=context.request.market,
            created_at=context.updated_at,
            latest_close=self._latest_close_value(context),
            overall_view=overall_view,
            confidence=confidence,
            horizon=context.request.horizon,
            executive_summary=executive_summary,
            investment_thesis=investment_thesis,
            base_case=base_case,
            bull_case_summary=bull_case_summary,
            bear_case_summary=bear_case_summary,
            what_to_watch=what_to_watch,
            agent_summaries=self._agent_summaries(context),
            risk_section=self._risk_section(context),
            data_quality_section=self._data_quality_section(context),
            source_section=self._source_items(context),
            cost_breakdown=self._cost_breakdown(context),
            warnings=warnings,
            disclaimer=DISCLAIMER,
            report_markdown=self._fallback_markdown(
                symbol=context.request.symbol,
                company_name=company_name,
                overall_view=overall_view,
                confidence=confidence,
                horizon=context.request.horizon,
                executive_summary=executive_summary,
                investment_thesis=investment_thesis,
                base_case=base_case,
                bull_case_summary=bull_case_summary,
                bear_case_summary=bear_case_summary,
                what_to_watch=what_to_watch,
                agent_summaries=self._agent_summaries(context),
                risk_section=self._risk_section(context),
                data_quality_section=self._data_quality_section(context),
                warnings=warnings,
            ),
        )
        return report

    def _agent_summaries(self, context: AnalysisContext) -> AgentSummaries:
        return AgentSummaries(
            technical=(
                context.technical_output.summary
                if context.technical_output is not None
                else "Technical Research Agent output is missing."
            ),
            news_sentiment=(
                context.news_sentiment_output.sentiment_summary
                if context.news_sentiment_output is not None
                else "News & Sentiment Agent output is missing."
            ),
            bull_case=(
                context.bull_case_output.bull_case
                if context.bull_case_output is not None
                else "Bull Case Agent output is missing."
            ),
            bear_case=(
                context.bear_case_output.bear_case
                if context.bear_case_output is not None
                else "Bear Case Agent output is missing."
            ),
            risk_review=(
                self._risk_review_summary(context.risk_review_output)
                if context.risk_review_output is not None
                else "Risk Review Agent output is missing."
            ),
        )

    def _risk_section(self, context: AnalysisContext) -> FinalReportRiskSection:
        if context.risk_review_output is None:
            return FinalReportRiskSection(
                risk_level="insufficient_data",
                risk_score=85,
                main_risks=["Risk Review Agent output is missing."],
                invalidation_conditions=[
                    "A completed risk review is needed to define reliable invalidation conditions."
                ],
                confidence_adjustment=-0.35,
            )
        risk = context.risk_review_output
        return FinalReportRiskSection(
            risk_level=risk.risk_level,
            risk_score=risk.risk_score,
            main_risks=list(risk.main_risks),
            invalidation_conditions=list(risk.invalidation_conditions),
            confidence_adjustment=risk.confidence_adjustment,
        )

    def _data_quality_section(
        self, context: AnalysisContext
    ) -> FinalReportDataQualitySection:
        summary = context.data_quality or DataQualitySummary()
        return FinalReportDataQualitySection(
            data_quality_score=self._raw_data_quality_score(context),
            price_data_status=summary.price_data_status,
            news_data_status=summary.news_data_status,
            company_profile_status=summary.fundamentals_status,
            missing_data=self._missing_data(context),
            providers=self._provider_names(context),
            warnings=self._data_quality_warnings(context),
        )

    def _source_items(self, context: AnalysisContext) -> list[FinalReportSourceItem]:
        items: list[FinalReportSourceItem] = []
        if context.market_data is not None:
            for source in context.market_data.sources:
                items.append(
                    FinalReportSourceItem(
                        name=source.name,
                        type=self._source_type(source.name),
                        provider=source.provider,
                        url=source.url,
                        used_for=self._source_used_for(source.name),
                    )
                )

            for news_item in context.market_data.news:
                items.append(
                    FinalReportSourceItem(
                        name=news_item.source,
                        type="news",
                        provider=news_item.source,
                        url=news_item.url,
                        used_for=f"News context: {news_item.title}",
                    )
                )

        if not items:
            return [
                FinalReportSourceItem(
                    name="Sources unavailable",
                    type="other",
                    provider="unavailable",
                    url=None,
                    used_for="Source metadata was unavailable in the analysis context.",
                )
            ]
        deduped: dict[tuple[str, str, str | None, str], FinalReportSourceItem] = {}
        for item in items:
            key = (item.name, item.provider, item.url, item.used_for)
            deduped.setdefault(key, item)
        return list(deduped.values())

    def _cost_breakdown(
        self,
        context: AnalysisContext,
        *,
        extra_trace: CostTrace | None = None,
    ) -> FinalReportCostBreakdown:
        traces = list(context.cost_traces)
        if extra_trace is not None:
            traces.append(extra_trace)
        items = [
            FinalReportCostItem(
                agent_name=trace.agent_name,
                provider=trace.provider,
                model=trace.model,
                input_tokens=trace.input_tokens,
                output_tokens=trace.output_tokens,
                estimated_cost_usd=trace.estimated_cost_usd,
                cost_type=trace.cost_type,
                duration_ms=trace.duration_ms,
                warnings=list(trace.warnings),
                parsing_errors=list(trace.parsing_errors),
            )
            for trace in traces
        ]
        return FinalReportCostBreakdown(
            total_estimated_cost_usd=round(
                sum(item.estimated_cost_usd for item in items), 8
            ),
            items=items,
        )

    def _report_warnings(self, context: AnalysisContext) -> list[str]:
        warnings = [
            *self._data_quality_warnings(context),
            *self._failed_or_missing_outputs(context),
        ]
        if not context.cost_traces:
            warnings.append("Cost tracking metadata was unavailable.")
        if context.market_data is None or not context.market_data.sources:
            warnings.append("Source metadata was unavailable.")
        if context.thesis_output is None:
            warnings.append("Thesis Agent output is missing.")
        return self._dedupe(warnings)

    def _risk_review_summary(
        self, risk_output: RiskReviewAgentOutput | None
    ) -> str:
        if risk_output is None:
            return "Risk Review Agent output is missing."
        main_risk = risk_output.main_risks[0] if risk_output.main_risks else "none"
        return (
            f"Risk level is {risk_output.risk_level} with a risk score of "
            f"{risk_output.risk_score:.0f}. Main risk: {main_risk}. "
            f"Confidence adjustment: {risk_output.confidence_adjustment:.2f}."
        )

    def _data_collector_output_payload(
        self, context: AnalysisContext
    ) -> dict[str, Any] | None:
        if context.market_data is None and context.data_quality is None:
            return None
        payload = DataCollectorOutput(
            market_data=context.market_data
            if context.market_data is not None
            else MarketDataBundle(
                symbol=context.request.symbol,
                market=context.request.market,
            ),
            data_quality=context.data_quality
            if context.data_quality is not None
            else DataQualitySummary(),
        ).model_dump(mode="json")

        market_payload = payload["market_data"]
        price_history = market_payload.get("price_history")
        if isinstance(price_history, list):
            market_payload["price_history_count"] = len(price_history)
            market_payload["price_history"] = price_history[-PROMPT_PRICE_HISTORY_LIMIT:]

        news_items = market_payload.get("news")
        if isinstance(news_items, list):
            market_payload["news_count"] = len(news_items)
            market_payload["news"] = news_items[:PROMPT_NEWS_LIMIT]

        sources = market_payload.get("sources")
        if isinstance(sources, list):
            market_payload["sources_count"] = len(sources)
            market_payload["sources"] = sources[:PROMPT_SOURCES_LIMIT]

        company_profile = market_payload.get("company_profile")
        if isinstance(company_profile, dict):
            company_profile.pop("description", None)

        return payload

