    def _source_type(self, source_name: str) -> str:
        name = source_name.strip().lower()
        if "news" in name or "rss" in name:
            return "news"
        if "price" in name or "quote" in name:
            return "price"
        if "profile" in name or "company" in name:
            return "profile"
        if "financial" in name or "sec" in name or "fact" in name:
            return "financials"
        return "other"

    def _source_used_for(self, source_name: str) -> str:
        source_type = self._source_type(source_name)
        mapping = {
            "price": "Price history and latest price context.",
            "news": "News and sentiment context.",
            "profile": "Company profile context.",
            "financials": "Fundamental and financial context.",
            "other": "General supporting context.",
        }
        return mapping[source_type]

    def _model_dump_or_none(self, value: Any) -> dict[str, Any] | None:
        if value is None:
            return None
        return value.model_dump(mode="json")

    def _join_or_none(self, values: Sequence[str]) -> str:
        if not values:
            return "none"
        return "; ".join(values)

    def _format_float(self, value: float) -> str:
        return f"{value:.2f}"

    def _format_datetime(self, value: datetime) -> str:
        return value.isoformat()

    def _dedupe(self, values: Sequence[str]) -> list[str]:
        deduped: dict[str, None] = {}
        for value in values:
            stripped = value.strip()
            if stripped:
                deduped.setdefault(stripped, None)
        return list(deduped)


